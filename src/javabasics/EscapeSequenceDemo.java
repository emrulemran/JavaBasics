package exceldemo;

public class EscapeSequenceDemo {

	public static void main(String[] args) {
		// double-quote example:
		System.out.println("the quote is: \"man is mortal\"");

		// single-quote example:
		System.out.println("This is New York's best street food!");

		// tab example:
		System.out.println("\tIts a tab example!");

		// new line example:
		System.out.println("One line and \nA new line!");
	}

}
